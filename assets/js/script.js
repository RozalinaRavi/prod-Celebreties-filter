window.addEventListener('load', function() {
    const userContainer = document.querySelector('.user-container');
    const genderRadio = document.querySelectorAll('.gender-radio');
    const ageRange = document.querySelector('#ageRange');
    const nameInput = document.querySelector('#nameFilter');

    function findMinAge(arr) {
        let minAge = Math.min(...arr.map(item => item.age))

        return minAge
    }

    function findMaxAge(arr) {
        let maxAge = Math.max(...arr.map(item=> item.age))

        return maxAge
    }

    let minAge = findMinAge(peopleArr)
    let MaxAge = findMaxAge(peopleArr)

    let config = {
        gender: 'all',
        age: '120',
        name: '',
        rangeVal: '40',
        rangeMin: '16',
        rangeMax: '80'
    }

    const setAgeFilter = (rangeVal, rangeMin, rangeMax) => {
        ageRange.setAttribute('value', config.rangeVal)
        ageRange.setAttribute('min', config.rangeMin)
        ageRange.setAttribute('max', config.rangeMax)
    }

    setAgeFilter()


    const renderPerson = (array) => {
        const list = array.map(person => {
            return `<div class="user">
                         <div class="user-photo">
                             <img src="${person.photo}" alt="${person.name}">
                         </div>

                         <div class="user-name">${person.name}</div>
                         <div class="user-age">${person.age}</div>
                         <div class="user-gender">${person.gender}</div>
                    </div>`
        }).join(``)
        userContainer.innerHTML = list
    }
    renderPerson(peopleArr)



    function getGenderVal() {
        genderRadio.forEach(item => {
            item.addEventListener('click', () => {
                let val = item.value
                config.gender = val

                //const filteredForGender = genderFilter()
                const filteredForGender = mainFilter()
                renderPerson(filteredForGender)
            })
        })
    }
    
    getGenderVal()

    function getNameVal() {
        nameInput.addEventListener('input', (event) => {
            let val = nameInput.value.toLowerCase().replace(/\s+/g, ' ').trim()
            config.name = val

            const filteredForName = mainFilter()
            renderPerson(filteredForName)
        })
    }
    getNameVal()

    function getAgeVal() {
        ageRange.addEventListener('click', () => {
            let val = ageRange.value
            config.age = val 

            //const filteredForAge = ageFilter()
            const filteredForAge = mainFilter()
            renderPerson(filteredForAge)
        })
    }
    getAgeVal()

    //function genderFilter() {
    //    return (config.gender === 'all' ? peopleArr : peopleArr.filter(item => item.gender === config.gender))
   // }

   //function nameFilter() {
   //     return peopleArr.filter((item) => item.name.toLowerCase().includes(config.name))
   // }

   // function ageFilter() {
   //     return peopleArr.filter(item => item.age <= config.age)
   // }

    function mainFilter() {
        const filtered = peopleArr.filter(item => {
            const setAge = item.age <= config.age
            const setGender = config.gender === 'all' ? peopleArr : item.gender === config.gender
            const setName = item.name.toLowerCase().includes(config.name)

            return setAge && setGender && setName
        })
        return filtered
    }
})

